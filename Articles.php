                    <h2 class="display-5 link-body-emphasis mb-1">Entrepreneurs Africains et levée de fonds : les travers du concours de beauté</h2>
                    <p class="blog-post-meta" style="text-align: justify!important;">par <em>Eric Ntonfo</em></p>
                    <p>Fondateur de Fiatope Invest, plateforme de financement participatif qui mobilise les diasporas africaines et épargnants européens pour accompagner des entreprises sur le Continent, innovantes, à fort impact sociale et environnemental
                        Il est également consultant pour des bailleurs de fonds internationaux comme la Banque Mondiale, l'AFD ou la Coopération belge sur les questions de financement de l'entrepreneuriat africain</p>
                    <hr>
                    <p></br>Chercher des fonds est à la mode chez les entrepreneurs et startups et notamment ceux du Continent. En effet ça fait en apparence chic de dire qu’on souhaite lever 100 000, 200 000 ou 500 000 Euros….Et ceux qui le disent sont persuadés de donner à leur projet beaucoup de valeurs aux yeux de ceux qui les écoutent et de gagner des points. A leur décharge, on leur explique souvent (les acteurs du financement) qu’en dessous de ces montants ils ne seront pas regardés.</p>
                    <p>Chers Entrepreneurs, j’ai une première mauvaise nouvelle pour nous ! Ce qui compte quand nous voulons être financés, c’est bien moins le montant de fonds que nous avançons, parfois de façon fantaisiste et non justifiée :</p>
                    <ul>
                        <li>Que l’évidence du problème que nous résolvons, sa persistance dans le temps ou le fait que la douleur touche un grand nombre de personnes;</li>
                        <li>Que notre capacité à convaincre sur la force de votre modèle économique;</li>
                        <li>Que notre aptitude à étayer notre maîtrise du marché ou à démontrer que notre approche commerciale est pertinente et puissante.</li>
                    </ul>
                    <p>Alors, mieux vaut parfois modestement dire qu’on a besoin de 10 000 ou 25 000 Euros pour améliorer son outil de production et avoir une chance de paraître crédible ( Surtout après avoir fait un pitch où on a expliqué qu’on existe depuis un an et qu’on a fait 5 000 Euros de CA), que de prétendre à 250 000 Euros pour construire une un site qui va concurrencer les acteurs du marché qui font 1 Millions d’Euros de chiffre d’affaires. Soyons sérieux!</p>
                    <p>Chers Entrepreneurs, j’ai une seconde mauvaise nouvelle pour nous ! Ce qui compte quand nous voulons être financés, c’est bien moins le nombre de personnes que nous affichons comme souhait de recrutement sur le slide deck, que notre faculté à montrer que nous nous appuyons sur des ressources ponctuelles, parfois prises dans nos cercles de connaissances ou notre réseau. Afin de nous apporter dans un premier temps de l’expertise que nous ne pouvons pas rémunérer dûment ou sur un temps plein. </br>Alors mieux vaut dire dans un pitch qu’on recherche 8 heures par semaine d’un freelance en développement ou 4h par semaine d’un juriste bénévole et avoir une chance de susciter de la confiance, que de dire que nous souhaitons recruter deux développeurs dont un back-end et un front-end, et un chargé des affaires juridiques. Attention aux talons haut perchés qui permettent une démarche impressionnante lors du concours de beauté mais qui cassent avant la fin du défilé.

                    </p>
                    <p>Chers entrepreneurs j’ai une troisième nouvelle qui va peut-être nous décevoir. Se développer à l’international n’est pas rédhibitoire pour la réussite de nos entreprises. C’est encore une vue de l’esprit imposée par le concours de beauté face au jury du capital risque. Notons que la plupart de nos grands entrepreneurs sur le Continent ont fait leur fortune sur un seul marché avant d’être connus sur le Continent! La sacro-saint scalabilité est sans doute exquise pour les panels de forums et de webinaires mais elle n’est pas la seule signature du succès.</br>

                        Alors mieux vaut dire qu’on cherche à consolider son marché dans le pays d’implantation et qu’on veut mieux y cibler ses clients qu’essayer de présenter un plan irréaliste de mise à l’échelle continentale.</p>

                    <p>Chers Entrepreneurs, j’ai une quatrième nouvelle et ce sera la dernière. Avoir un co-fondateur n’a jamais garanti la performance. Ce qui compte c’est bien moins le fait d'en exhiber un coûte que coûte que de faire de votre équipe, de vos recrues, un bloc mobilisé, engagé et soudé pour la réussite du projet.</br>
                        Alors mieux vaut jouer franc jeu, s’affirmer en leader unique et montrer qu’on est capable d’emmener l’équipe vers la vision tracée.</p>

                    <p>Enfin chers Entrepreneurs, j’ai une excellente nouvelle pour nous ! Le succès ne dépend pas nécessairement de notre disposition à lever des fonds de capital risque ou de venture capital. Il dépend plutôt de notre bonne jugeote face à nos besoins de développement et de notre intelligence dans la façon de les adresser. A titre d’exemples :</br>
                    <ul>
                        <li>Se développer avec des collaborateurs qui comprennent l’esprit entrepreneurial, c’est-à-dire qui s’investissent et modulent leurs exigences par rapport aux capacités financières de la startup</li>
                        <li>Inculquer à l’équipe et s’imposer à soi en tant que fondateur l’idée que tous les coûts opérationnels doivent être réduits à leur plus petite valeur possible.</li>
                        <li>Cultiver la polyvalence: une fois le produit devenu mature, le membre de l’équipe qui tenait la fonction de chef de produit peut évoluer vers celle de busines développer et chargé de relation client, ce qui permet de pourvoir ces rôles sans recruter.</li>
                        <li>Se positionner pour des subventions et des appels à projet, chaque fois que l’opportunité se présente et qu’elle est pertinente pour la startup, sans y consacrer plus de 5 à 10% du temps opérationnel utile.
                            Etc.</li>
                        </br>Disons nous une chose et une fois pour toutes: nous aurons le succès pas parce que nous aurons recruté 3 cadors, ni parce que nous nous serons développés à l’international, ni encore moins parce que nous aurons levé 500 00 Euros, ni d’ailleurs parce que nous aurons trouvé un co-fondateur. Ce sera parce que nous aurons mis en exergue une approche très prometteuse pour dérouler notre modèle économique.</p>
