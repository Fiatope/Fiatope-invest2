                    <p>
                        Toujours un plaisir de venir ici délivrer mes quelques pièces de centimes, pour traduire mot à mot l’expression américaine “deliver my few cents”, qui signifie donner son humble avis sur un sujet.</br>

                        </br> Aujourd’hui, je vais parler de l’art du pitch d’entrepreneur.</br>

                        </br> Quand mon père me disait il y a 30 ans : « Fils, un bon élève de Tle C doit aussi être bon en français et faire de belles dissertations », je l’écoutais d’une oreille en me disant : « Mouais… toi, le littéraire, tu ne peux pas dire autre chose. »
                        Plus tard, arrivé en école d’ingénieur, lorsqu’il me disait : « Fils, un très bon ingénieur doit pouvoir très bien s’exprimer », je commençais à l’écouter avec plus de sérieux.</br>

                        </br>Eh bien, on y est. La première chose, avant même d’apprendre à pitcher, est de travailler l’éloquence de son expression. Et cela, beaucoup de nos entrepreneurs l’oublient. Bien parler, bien articuler, avoir le bon timbre de voix… cela fige toujours votre audience. Testez, et vous m’en direz des nouvelles.</br>

                        </br>La seconde chose est de peaufiner votre charisme. Mais encore faut-il savoir de quel type de charisme vous êtes fait : physique (la taille notamment, ou le regard), vestimentaire, verbal, empathique… Il est important d’identifier lequel est le vôtre et de l’utiliser intelligemment. Votre charisme contribuera fortement à embarquer votre audience dans ce que vous avez à lui raconter. C’est un constituant essentiel de votre leadership.</br>

                        </br>La troisième chose mais en réalité la première en importance c’est la structure de votre pitch et sa dramaturgie, autrement dit sa capacité à garder votre audience en haleine. Vous avez en général peu de temps pour capter l’attention. Alors ne loupez pas votre mise en scène, sans en faire too much, comme il m’arrive de le voir souvent.</br>

                        </br>La quatrième et dernière chose dont je vais parler aujourd’hui, c’est l’émotion que vous allez dégager. La bonne nouvelle, c’est que le quotient émotionnel, tout comme les attributs précédents, se travaille aussi. Là encore, soyez naturel·le ! Ne surjouez pas l’émotion. Ça sonne en général très faux, et cela peut même gêner.</br>

                        </br>Onze startups étaient invitées à mettre leurs qualités en œuvre il y a quelques jours à l’occasion d’un pitch contest organisé par deux très belles initiatives : Enovation Factory et The Okwelians.</br>

                        </br>En tant que co-président du jury, j’ai pris une grande joie à écouter bon nombre de ces pitches, issus d’entreprises accompagnées des deux côtés. Mais j’ai aussi dû jouer au petit méchant en les arrêtant quand il le fallait, c’est-à-dire à la 300ᵉ seconde.
                        À ce sujet, chers entrepreneurs : entraînez-vous à finir à 4'45 quand vous avez 5 minutes. Ce qui n’est pas dit en 5 minutes ne le sera pas dans la minute supplémentaire que vous allez gratter en faisant semblant de ne pas voir les grandes mains du président s’agiter</br>
