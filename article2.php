                    <h2 class="display-5  mb-1" style="font-size : 35px; font-weight: 370;">La solitude du fondateur : entre quête de </br> co-fondateur et responsabilités.</h2>
                    <p class="blog-post-meta" style="text-align: justify!important;"> </br>par <em>Eric Ntonfo</em></p>
                    <p>Fondateur de Fiatope Invest, plateforme de financement participatif qui mobilise les diasporas africaines et épargnants européens pour accompagner des entreprises sur le Continent, innovantes, à fort impact sociale et environnemental
                        Il est également consultant pour des bailleurs de fonds internationaux comme la Banque Mondiale, l'AFD ou la Coopération belge sur les questions de financement de l'entrepreneuriat africain</p>
                    <hr>
                    <p>Comme annoncé, je reviens avec un article sur la solitude de l’entrepreneur. Je reste dans cette mouvance qui consiste à raconter l’entrepreneuriat tel qu’il se déroule réellement, sans paillettes.</br>

                        </br>Je vais commencer par la difficulté de trouver un cofondateur. J’ai consacré une partie non négligeable des premières années de l’aventure à chercher une personne que je pouvais embarquer à la même hauteur que moi ou, à défaut, à un niveau qui fasse qu’on soit deux dans la barque, même si je devais pagayer plus. Cette recherche s’est avérée extrêmement compliquée, et pour plusieurs raisons :</br>
                    <ul>

                        <li>Je me suis rendu compte que cela faisait peur de s’engager aux côtés d’une personne qui semblait déjà avoir construit beaucoup. Peur de ne pas être à la hauteur, peur que la masse de travail soit terrassante, ou tout simplement peur de l’inconnu. Le plus frustrant, en réalité, est que cette peur est irrationnelle, et qu’il n’y a, je pense, rien d’extraordinaire à se trouver dans cette situation. Parfois, il s’agit de soutenir l’activité quelques heures par semaine, ce que naturellement on fait lorsqu’on a, par exemple, une passion.</li>

                        </br>
                        <li>La deuxième raison que j’ai observée, c’est que lorsque l’on n’a pas été présent dans la formulation d’une idée ou dans sa genèse, on peut trouver inconfortable d’évoluer auprès de celui ou celle qui en est à l’origine ou qui l’a longtemps portée seul·e. Ce sentiment peut être légitime.</li>

                    </ul>
                    </br>Le grand enseignement de cette difficile recherche du cofondateur est que co-fonder, c’est véritablement construire, façonner, mouler avec. Et si on l’a fait seul au départ, eh bien on est fondateur. Point barre.</br>

                    </br>Je vais embrayer sur la notion de responsabilité et l’esprit de sacrifice. J’ai pensé pendant longtemps que je pouvais embarquer certains collaborateurs dans la conduite de l’organisation. Certes, j’ai eu quelques collaborateurs qui, durant leur passage chez nous, ont fait preuve d’une dévotion et d’un engagement de bonne facture. Mais, de façon générale, je n’ai pas le niveau d’implication et d’appropriation que j’attends d’eux. Peut-être suis-je trop exigeant.</br>

                    </br>En attendant de le savoir, je tire la conclusion qu’un collaborateur reste un collaborateur, animé comme le formulait déjà la théorie des organisations depuis longtemps par « le désir d’augmentation salariale et de promotion ». Encore que tous n’acceptent pas la promotion si elle doit s’accompagner d’un engagement et d’une responsabilisation supplémentaires.</br>

                    </br>Cependant, je vais terminer sur une belle nouvelle. L’entrepreneur n’est pas si seul que ça ! En effet, je chéris tous les jours la présence, dans mon entourage, de parents, de frères, sœurs, amis, connexions LinkedIn, d’une conjointe, qui vous financent, vous encouragent, vous font vous sentir comme un champion, vous remontent le moral par les temps difficiles, vous galvanisent, vous aident dans les choix cornéliens. Eh oui, vous êtes en réalité nos multiples cofondateurs, et sans vous, il n’y aurait simplement pas d’aventure. Un grand merci.</p>
