                    <h2 class="display-5 mb-1" style="font-size : 35px; font-weight: 350;">Innover sans immersion : l’erreur stratégique </br>qui coûte cher.</h2>
                    <p class="blog-post-meta" style="text-align: justify!important;"> </br>par <em>Eric Ntonfo</em></p>
                    <p>Fondateur de Fiatope Invest, plateforme de financement participatif qui mobilise les diasporas africaines et épargnants européens pour accompagner des entreprises sur le Continent, innovantes, à fort impact sociale et environnemental
                        Il est également consultant pour des bailleurs de fonds internationaux comme la Banque Mondiale, l'AFD ou la Coopération belge sur les questions de financement de l'entrepreneuriat africain</p>
                    <hr>
                    <p>Innover sur un marché demande non pas seulement de comprendre approximativement ce marché, mais d’y être imprégné.</br>

                        </br>Lorsque je travaillais à la Direction Innovation Afrique d’un grand opérateur télécom à Paris, j’ai souvent eu l’occasion de donner un avis tranché sur la pertinence des lancements de produits sur le marché.</br>

                        </br> Un exemple qui me revient à l’esprit est celui de la messagerie vocale. Nous avions, dans notre roadmap, le lancement de ce produit pour cinq ou six pays. Il s’agit de cette fonctionnalité qui permet, lorsqu’une personne vous a appelé en absence, de vous laisser un message.</br>

                        </br>Ma petite connaissance du marché local en Côte d’Ivoire et au Cameroun, où je séjournais beaucoup, me permit d’expliquer au patron de l’entité et aux équipes produit que, dans ces deux pays au moins, le lancement de ce service générerait peu d’usage. En effet, la culture vis-à-vis des appels consistait, lorsque le correspondant ne décrochait pas, non pas à déclencher une consommation de crédit en laissant un message, mais plutôt à se faire rappeler.</br>

                        </br> En environnement prépayé, où chaque minute de communication compte, et où la tarification de l’accès à la messagerie n’était pas connue d’avance, il était difficile de susciter l’adhésion. Ce sont les arguments que j’avais mis en avant au comité produit, animé et dirigé par des personnes qui n’avaient pas toujours une immersion suffisante dans le marché pour lequel ils devaient valider l’opportunité du lancement. Le produit fut tout de même lancé.</br>

                        </br> Je pris alors sur moi d’emmener les équipes produit à travailler un parcours client qui soit le plus optimal et pédagogique possible, afin d’une part de susciter l’envie de laisser le fameux message, et d’autre part d’en comprendre la tarification. C’est à ce stade que les équipes locales, cette fois, eurent tout le mal du monde à comprendre cette logique d’optimalité et lancèrent la messagerie vocale avec un parcours finalement très dissuadant, malgré tout le combat que j’avais pu mener.</br>

                        </br>Le consommateur sonnait son correspondant, puis entendait une voix de l’opérateur lui disant être « désolé » que l’abonné ne soit pas joignable. Cette négativité, à elle seule, rebutait. Ensuite, la voix l’invitait à laisser un message après le signal. Sauf qu’à ce moment précis, le message de la boîte vocale de son correspondant démarrait. Résultat : 99 % des clients raccrochaient.</br>

                        </br> Aujourd’hui encore, douze ans après, la messagerie vocale n’a jamais réellement pris sur ces deux marchés. Avec l’arrivée de la voix sur IP et des messageries instantanées, la note vocale s’est finalement imposée comme messagerie vocale, comme c’est d’usage sur WhatsApp par exemple. Avec, en prime, le fait qu’il ne soit pas nécessaire de sonner son correspondant pour la déposer, et donc la possibilité de le faire sans le déranger.</br>

                        </br> Je voulais montrer, au travers de cette anecdote, à quel point il est important d’écouter un marché et de le vivre un minimum pour pouvoir y innover.</p>
